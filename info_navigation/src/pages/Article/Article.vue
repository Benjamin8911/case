					<p>例如，商品期货交易委员会（CFTC）素有“对加密货币友好的监管机构”的美誉，但美国证券交易委员会(SecurityExchangeCommission)在其与加密货币有关的声明中变得更加强硬。目前，该机构正在对加密货币交易所进行审查，并表示正在寻求对交易所和密码钱包适用的证券法。</p>
