					<p>然而，并非所有欧洲国家都如此。例如，法国公布的一份数字加密货币黑名单，这表明许多邻国都倾向于严格监管。巴黎也在上个月20国集团会议之前与柏林联合起来，呼吁加强对数字加密货币的监管，因为这些货币“给投资者带来了巨大风险”。</p>
					<p>亚洲立法者严厉打击</p>
					<p>世界上大部分的数字货币交易都发生在亚洲，该地区也是一些最严厉的数字货币立法的所在地——不仅仅是在中国。</p>
					<p>在柬埔寨，国家银行认为“数字货币”是非法的。更重要的是，就全球市场而言，印度尼西亚银行最近禁止使用数字加密货币作为支付工具。韩国和泰国似乎也在朝着更严格的监管方向发展。在韩国，代币发行已被禁止，尽管政府仍在决定如何对交易所进行立法，但监管机构可能会加强监管。</p>
					<p>菲律宾也正在起草立法，以加强对数字加密货币的控制。印度最近对数字加密货币进行了打击，禁止该国的银行从事虚拟货币交易。</p>
					<p>与邻国相比，日本是一个“异类”，因为政府对数字加密密货币的态度相当积极。这个国家是世界上最大的比特币市场，并且数字货币被认为是法定货币。尽管日本实行了管制立法，但与邻国的规则相比，其许可证制度是宽松的。</p>
					<p>从许多方面来说，日本的数字加密立法是未来世界范围内立法的一个试验案例，但上月两个数字加密货币的被叫停表明，东京可能根本没有达到适当的平衡。</p>
