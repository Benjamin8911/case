					<p>与瑞士一样，马耳他政府对虚拟货币和他们依赖的区块链技术表现出了极大的热情，旨在成为数字加密货币交易所的“全球先锋”。</p>
					<p>由于在线博彩业对马耳他的经济非常重要，占国内生产总值的12%，该国的在线监管机构目前正在敲定一项监管“沙箱”计划，该计划将允许在线博彩运营商在其支付选项列表中添加密码。</p>
