					<p>以中国为例。去年年底，中国出台了一系列严格的比特币和其他数字加密货币新规，有效地阻止了虚拟的数字货币交易，并禁止了首次代币发行，而这正是新数字加密货币调集资金的方式。但它并未阻止数字加密货币的流通。</p>
					<p>事实上，全球数字加密货币产业存在着法律灰色地带，在这一领域造成混乱的一个原因是各国的立法大相径庭。</p>
					<p>从本质上来看，全球数字加密货币是及其去中心化的，分布在世界各地，只要有一台矿机、一个交易所或一对人在交易，那么它就会存在，然而世界各地政府对加密货币的监管政策差异很大，因此对于世界级别的加密货币扩散，一个国家的政府能做到的监管是局限的。</p>
